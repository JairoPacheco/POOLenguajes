package Clases;

/**
 *
 * @author Jus
 */
public class Imagen {
    String direccion;
    String tamaño;
    String nombre;

    public Imagen(String direccion, String tamaño, String nombre) {
        this.direccion = direccion;
        this.tamaño = tamaño;
        this.nombre = nombre;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTamaño() {
        return tamaño;
    }

    public void setTamaño(String tamaño) {
        this.tamaño = tamaño;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
